#include <iostream>
using namespace std;
int a[30][30];
int main()
{int n,m,c,b,q,w;
cin>>n>>m>>c>>b;
if(((c==0)&&(b==0))||((c==n)&&(b==m)))
cout<<0;
else
if(((c-2==0)&&(b-1==0))||((c-1==0)&&(b-2==0)))
cout<<0;
else
if((c!=0)||(b!=0))
{for(q=0;q<=n;q++)
a[q][0]=1;
for(w=0;w<=m;w++)
a[0][w]=1;
if(c==0)
for(q=b;q<=m;q++)
a[0][q]=0;
if(b==0)
for(q=c;q<=n;q++)
a[q][0]=0;
if(c-2==0)
for(q=b+1;q<=m;q++)
a[0][q]=0;
if(c-1==0)
for(q=b+2;q<=m;q++)
a[0][q]=0;
if(b-2==0)
for(q=c+1;q<=n;q++)
a[q][0]=0;
if(b-1==0)
for(q=c+2;q<=n;q++)
a[q][0]=0;
if((c-2==0)&&(b-1>0))
for(q=b;q<=m;q++)
a[0][q]=0;
if((c-1==0)&&(b-2>0))
for(q=b;q<=m;q++)
a[0][q]=0;
if((b-2==0)&&(c-1>0))
for(q=c;q<=n;q++)
a[q][0]=0;
if((b-1==0)&&(c-2>0))
for(q=c;q<=n;q++)
a[q][0]=0;
a[c][b]=0;
if((c>=2)&&(b>=1))
a[c-2][b-1]=0;
if(c>=2)
a[c-2][b+1]=0;
a[c+2][b+1]=0;
if(b>=1)
a[c+2][b-1]=0;
if(c>=1)
a[c-1][b+2]=0;
if((b>=2)&&(c>=1))
a[c-1][b-2]=0;
if(b>=2)
a[c+1][b-2]=0;
a[c+1][b+2]=0;
for(q=1;q<=n;q++)
for(w=1;w<=m;w++)
{a[q][w]=a[q-1][w]+a[q][w-1];
a[c][b]=0;
if((c>=2)&&(b>=1))
a[c-2][b-1]=0;
if(c>=2)
a[c-2][b+1]=0;
a[c+2][b+1]=0;
if(b>=1)
a[c+2][b-1]=0;
if(c>=1)
a[c-1][b+2]=0;
if((b>=2)&&(c>=1))
a[c-1][b-2]=0;
if(b>=2)
a[c+1][b-2]=0;
a[c+1][b+2]=0;
}
cout<<a[n][m];
}
return 0;
}
